using System;

// Variable is a memory area that keeps a value, that can be changed.

namespace TypeBasics
{
    class Program
    {
        static void Main()
        {
            // On line 13 we created a byte type variable, named "age" and assigned a value of 25 to it.

            byte age = 25;

            // On line 17 we display the value of the variable "age".

            Console.WriteLine(age);

            // Delay.
            Console.ReadKey();
        }
    }
}
